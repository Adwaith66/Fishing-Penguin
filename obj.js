function getVARR(part){
    let vertices = []
    let textures = []
    let normals = []

    let obj = []
    let texture = []
    let normal = []

    let arr = getString(part).split('\n')
    let v1, v2, v3, t1, t2, t3, f1, f2, f3;
    for (let v of arr) {
        v = v.trim()
        if (v.startsWith('v') && !v.startsWith('vt') && !v.startsWith('vn')){
            [, v1, v2, v3] = v.split(/\s+/)
            vertices.push(v1, v2, v3)
        }
        else if (v.startsWith('vt')) {
            [, v1, v2] = v.split(/\s+/)
            textures.push(v1, v2)
        }
        else if (v.startsWith('vn')) {
            [, v1, v2, v3] = v.split(/\s+/)
            normals.push(v1, v2, v3)
        }
        else if (v.startsWith('f')){
            [, f1, f2, f3] = v.split(/\s+/)
            v1 = beforeChar(f1)
            v2 = beforeChar(f2)
            v3 = beforeChar(f3)

            t1 = betweenChars(f1)
            t2 = betweenChars(f2)
            t3 = betweenChars(f3)

            n1 = afterChar(f1)
            n2 = afterChar(f2)
            n3 = afterChar(f3)





            for (var vx of [v1, v2, v3]){
                obj = obj.concat(vertices.slice((vx-1)*3, (vx-1)*3+3))
            }

            for (var tx of [t1, t2, t3]){
                texture = texture.concat(textures.slice((tx-1)*2, (tx-1)*2+2))

            }

            for (var nx of [n1, n2, n3]){
                normal = normal.concat(normals.slice((nx-1)*3, (nx-1)*3+3))
            }

        }
    }
    
    return [obj, texture, normal]
}


function beforeChar(input) {
    const index = input.indexOf('/');
    if (index !== -1) {
        return(input.slice(0, index));
    } else {
        return(input);
    }
}

function betweenChars(input) {
    const start = input.indexOf('/');
    const end = input.lastIndexOf('/');

    if (start !== -1 && end !== -1) {
        return input.slice(start + 1, end);
    } else {
        return "";
    }
}


function afterChar(input) {
    const end = input.lastIndexOf('/');

    if (end !== -1) {
        return input.slice(end+ 1);
    } else {
        return "";
    }
}

